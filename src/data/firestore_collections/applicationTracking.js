// Funciones de utilidad para calcular estadísticas
export const getApplicationStats = () => {
  const total = applicationTrackingData.length;
  const postulados = applicationTrackingData.filter(app => app.status === "postulados").length;
  const entrevistas = applicationTrackingData.filter(app => app.status === "entrevistas").length;
  const rechazados = applicationTrackingData.filter(app => app.status === "rechazados").length;
  const aceptados = applicationTrackingData.filter(app => app.status === "aceptados").length;

  return {
    total,
    postulados,
    entrevistas,
    rechazados,
    aceptados
  };
};

// Función para calcular empleos conseguidos (aceptados)
export const getJobsObtained = () => {
  return applicationTrackingData.filter(app => app.status === "aceptados").length;
};

// Función para calcular entrevistas conseguidas (entrevistas + aceptados)
export const getInterviewsObtained = () => {
  return applicationTrackingData.filter(app => 
    app.status === "entrevistas" || app.status === "aceptados"
  ).length;
};

// Función para obtener estadísticas por estado
export const getApplicationStatusStats = () => {
  const stats = getApplicationStats();
  return [
    { name: "Postulados", value: stats.postulados, color: "#028bbf" },
    { name: "Entrevistas", value: stats.entrevistas, color: "#f59e0b" },
    { name: "Rechazados", value: stats.rechazados, color: "#ef4444" },
    { name: "Aceptados", value: stats.aceptados, color: "#10b981" }
  ];
};